      call throw_exception(NO_MODIFICATION_ALLOWED_ERR, "removeAttribute", ex)
if (present(ex)) then
  if (inException(ex)) then
     return
  endif
endif

    endif
    
    if (arg%inDocument) &
      call setGCstate(getOwnerDocument(arg), .false.)

    dummy => removeNamedItem(getAttributes(arg), name, ex2)
